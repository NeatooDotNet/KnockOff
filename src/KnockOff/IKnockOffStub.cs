namespace KnockOff;

/// <summary>
/// Marker interface implemented by all KnockOff-generated stubs.
/// Enables extension methods and common stub operations.
/// </summary>
public interface IKnockOffStub
{
    /// <summary>
    /// When true, unconfigured method calls throw <see cref="StubException"/> instead of returning default.
    /// </summary>
    bool Strict { get; set; }
}
